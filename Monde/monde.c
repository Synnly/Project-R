#include "monde.h"

/* ==================================================================================================================================  */
/* ============================================================  Getters ============================================================  */
/* ==================================================================================================================================  */

joueur* getJoueur(monde* Monde){return Monde->Joueur;}
bool isGamePlaying(monde* Monde){return Monde->enJeu;}

/* ==================================================================================================================================  */
/* ============================================================  Setters ============================================================  */
/* ==================================================================================================================================  */

void setJoueur(monde* Monde, joueur* Joueur){Monde->Joueur=Joueur;}
void startGame(monde* Monde){Monde->enJeu = true;}
void finishGame(monde* Monde){ Monde->enJeu = false;}

/* ==================================================================================================================================  */
/* ===========================================================  Fonctions ===========================================================  */
/* ==================================================================================================================================  */

monde* initMonde(){
    monde Monde;
    joueur Joueur = initJoueur(100, 100, 20, 20);
    setJoueur(&Monde, &Joueur);
    startGame(&Monde);

    return &Monde;
}
